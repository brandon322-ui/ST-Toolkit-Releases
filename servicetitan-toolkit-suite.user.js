  function findInstalledOnInput(root = document) {
    const inputs = [...root.querySelectorAll('input')]
      .filter(input => input.offsetParent !== null);

    const semanticInput = inputs.find(input => {
      const identity = [
        getAccessibleName(input),
        input.id,
        input.name,
        input.getAttribute('aria-label'),
        input.getAttribute('data-testid'),
        input.getAttribute('data-field'),
        input.getAttribute('data-bind')
      ].filter(Boolean).join(' ');

      return /\binstalled[\s_-]*on\b/i.test(identity);
    });
    if (semanticInput) return semanticInput;

    const installedLabel = [...root.querySelectorAll('label, [data-anvil-component="Label"]')]
      .filter(label => label.offsetParent !== null)
      .find(label => /^installed on$/i.test(normalizeSpaces(label.innerText)));

    if (installedLabel) {
      const associated = getLabelControl(installedLabel, root);
      if (associated?.matches('input')) return associated;

      const fieldRoot = installedLabel.closest([
        '[data-anvil-component="FormField"]',
        '.FormField',
        '.Field',
        '.InputGroup',
        '[role="group"]',
        'div'
      ].join(','));
      const fieldInput = [...(fieldRoot?.querySelectorAll('input') || [])]
        .find(input => input.offsetParent !== null);
      if (fieldInput) return fieldInput;
    }

    // Compatibility fallback for the older date picker markup. Installed On is
    // the first populated date input in the edit form; Manufactured On follows it.
    return inputs.find(input =>
      input.matches(ST.dateInputs) && /^\d{1,2}\/\d{1,2}\/\d{4}$/.test(input.value || '')
    ) || null;
  }

