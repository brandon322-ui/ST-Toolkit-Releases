    async function waitForMaterialDeleted(materialId, invoiceNumber) {
        const timeout = 60000;
        const start = Date.now();

        while (Date.now() - start < timeout) {
            const materials = getInvoiceMaterials(await fetchInvoiceData(invoiceNumber));
            if (!materials.some(material => Number(material.Id) === Number(materialId))) {
                return;
            }

            await sleep(750);
        }

        throw new Error(`Timed out waiting for material ${materialId} to be removed.`);
    }

