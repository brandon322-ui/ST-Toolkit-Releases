    function startDrag(e) {
        if (e.target.closest('button')) return;

        const box = document.getElementById(TOOL_ID);
        if (!box) return;

        dragState = {
            box,
            offsetX: e.clientX - box.offsetLeft,
            offsetY: e.clientY - box.offsetTop
        };

        e.preventDefault();
    }

    function moveDrag(e) {
        if (!dragState) return;

        const left = Math.max(0, e.clientX - dragState.offsetX);
        const top = Math.max(0, e.clientY - dragState.offsetY);

        dragState.box.style.left = left + 'px';
        dragState.box.style.top = top + 'px';
        dragState.box.style.right = 'auto';
        dragState.box.style.bottom = 'auto';
    }

    function endDrag() {
        if (!dragState) return;

        const box = dragState.box;
        const left = box.offsetLeft;
        const top = box.offsetTop;

        dragState = null;

        if (shouldSnapToDock(left, top)) {
            snapToDock(box);
            setToolkitMessage('Toolkit snapped to dock.');
            setTimeout(createBox, 100);
            return;
        }

        Store.setDocked(false);
        Store.savePosition(left, top);
    }

