  function clampPanelPosition(panel, left, top) {
    const maxLeft = Math.max(0, (window.innerWidth || 0) - (panel?.offsetWidth || 420));
    const maxTop = Math.max(0, (window.innerHeight || 0) - Math.min(panel?.offsetHeight || 100, 100));

    return {
      left: Math.min(Math.max(0, Math.round(left)), maxLeft),
      top: Math.min(Math.max(0, Math.round(top)), maxTop)
    };
  }

  function makeDraggable(panel, handle, onPositionChange) {
    let dragging = false;
    let ox = 0;
    let oy = 0;

    const dock = { left: 120, top: 50 };
    const snapDistance = 70;

    function applyDock() {
      panel.style.left = `${dock.left}px`;
      panel.style.top = `${dock.top}px`;
      panel.style.right = 'auto';
      panel.style.bottom = 'auto';
      onPositionChange?.();
    }

    function isNearDock() {
      const r = panel.getBoundingClientRect();
      return Math.abs(r.left - dock.left) <= snapDistance && Math.abs(r.top - dock.top) <= snapDistance;
    }

    const onMouseDown = e => {
      dragging = true;
      ox = e.clientX - panel.offsetLeft;
      oy = e.clientY - panel.offsetTop;
    };

    const onMouseMove = e => {
      if (!dragging) return;

      const position = clampPanelPosition(panel, e.clientX - ox, e.clientY - oy);
      panel.style.left = `${position.left}px`;
      panel.style.top = `${position.top}px`;
      panel.style.right = 'auto';
      panel.style.bottom = 'auto';
    };

    const onMouseUp = () => {
      if (!dragging) return;

      dragging = false;
      if (isNearDock()) applyDock();
      else onPositionChange?.();
    };

    handle.addEventListener('mousedown', onMouseDown);
    document.addEventListener('mousemove', onMouseMove);
    document.addEventListener('mouseup', onMouseUp);

    return () => {
      dragging = false;
      handle.removeEventListener('mousedown', onMouseDown);
      document.removeEventListener('mousemove', onMouseMove);
      document.removeEventListener('mouseup', onMouseUp);
    };
  }

