    function waitForCondition(check, { timeout = 30000, message = 'Timed out waiting for ServiceTitan.' } = {}) {
        return new Promise((resolve, reject) => {
            let activeElapsed = 0;
            let lastTick = Date.now();
            let settled = false;
            let observer = null;
            let timer = null;

            const cleanup = () => {
                settled = true;
                observer?.disconnect();
                if (timer) clearTimeout(timer);
            };

            const evaluate = () => {
                if (settled) return;

                try {
                    const result = check();
                    if (result) {
                        cleanup();
                        resolve(result);
                        return;
                    }
                } catch (err) {
                    cleanup();
                    reject(err);
                    return;
                }

                const now = Date.now();
                if (!document.hidden) {
                    activeElapsed += now - lastTick;
                }
                lastTick = now;

                if (activeElapsed >= timeout) {
                    cleanup();
                    reject(new Error(message));
                    return;
                }

                timer = setTimeout(evaluate, 250);
            };

            if (document.body) {
                observer = new MutationObserver(evaluate);
                observer.observe(document.body, {
                    childList: true,
                    subtree: true,
                    attributes: true,
                    characterData: true
                });
            }

            evaluate();
        });
    }

    function getTablesInNamedSection(sectionName) {
        const headings = [...document.querySelectorAll(
            'h1, h2, h3, h4, h5, h6, [role="heading"], .section-title, .card-title'
        )];
        const sectionHeading = headings.find(heading =>
            String(heading.innerText || heading.textContent || '').replace(/\s+/g, ' ').trim() === sectionName
        );

        if (!sectionHeading) return [];

        const nextHeading = headings.find(heading =>
            heading !== sectionHeading &&
            Boolean(sectionHeading.compareDocumentPosition(heading) & 4)
        );

        return [...document.querySelectorAll('table')].filter(table => {
            const followsSectionHeading = Boolean(sectionHeading.compareDocumentPosition(table) & 4);
            const precedesNextHeading = !nextHeading || Boolean(table.compareDocumentPosition(nextHeading) & 4);
            return followsSectionHeading && precedesNextHeading;
        });
    }

