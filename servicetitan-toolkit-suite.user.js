    function isQuantityLabel(text) {
        return /^(quantity|qty)$/i.test(String(text || '').replace(/\s+/g, ' ').trim());
    }

    function getHeaderColumnIndex(headerCell) {
        const columnHeader = headerCell.matches('th, td, [role="columnheader"]')
            ? headerCell
            : headerCell.closest('th, td, [role="columnheader"]');
        if (!columnHeader) return -1;

        const siblings = [...(columnHeader.parentElement?.children || [])];
        const position = siblings.indexOf(columnHeader);

        if (position < 0) return -1;

        return siblings
            .slice(0, position)
            .reduce((index, cell) => index + Math.max(Number(cell.colSpan) || 1, 1), 0);
    }

    function getQtyCell(row) {
        const cells = [...row.querySelectorAll(':scope > td, :scope > th')];

        const quantityBoundCell = row.querySelector(
            'td[data-bind*="formatNumber(Quantity"]'
        );
        if (quantityBoundCell) return quantityBoundCell;

        const labeledCell = cells.find(cell =>
            [cell.dataset.label, cell.dataset.title, cell.getAttribute('aria-label')]
                .some(isQuantityLabel)
        );
        if (labeledCell) return labeledCell;

        const table = row.closest('table');
        const quantityHeader = [...(table?.querySelectorAll(
            'thead th, thead td, [role="columnheader"], thead div, thead span'
        ) || [])]
            .find(header => isQuantityLabel(header.innerText || header.textContent));

        if (quantityHeader) {
            const quantityIndex = getHeaderColumnIndex(quantityHeader);
            if (quantityIndex >= 0 && cells[quantityIndex]) return cells[quantityIndex];
        }

        // Compatibility fallback for the current ServiceTitan materials layout:
        // Name, Code, Description, Quantity. Only accept a plain numeric value so
        // a currency cell such as "$1.00" cannot be mistaken for quantity.
        const currentLayoutCell = cells[3] || null;
        const currentLayoutText = currentLayoutCell?.innerText.trim().replace(/,/g, '') || '';

        return /^[+-]?(?:\d+(?:\.\d+)?|\.\d+)$/.test(currentLayoutText)
            ? currentLayoutCell
            : null;
    }

    function isBadMaterialRow(row) {
        const rowText = (row.innerText || '').replace(/\s+/g, ' ').trim().toLowerCase();
        const badPattern = /\bns\b.*\bnonstock\b.*\$1\b.*\bpart\b.*\badjust\b.*\bquantity\b.*\bnon[- ]stock\b/;
        if (badPattern.test(rowText)) {
            return true;
        }

        const qtyCell = getQtyCell(row);
        if (!qtyCell) return false;

        const qtyText = qtyCell.innerText.trim().replace(/,/g, '');
        if (qtyText === '0.00' || qtyText === '0') {
            return true;
        }

        const qty = Number(qtyText);
        return Number.isFinite(qty) && !Number.isInteger(qty);
    }

    function getBadMaterialRows() {
        return getMaterialsRows().filter(isBadMaterialRow);
    }

