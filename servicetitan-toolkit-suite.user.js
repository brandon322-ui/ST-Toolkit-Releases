  function normalizeDate(text) {
    const m = String(text || '').trim().match(/^(\d{1,2})\/(\d{1,2})\/(\d{4})$/);
    if (!m) return text;
    return `${m[1].padStart(2, '0')}/${m[2].padStart(2, '0')}/${m[3]}`;
  }

  function getYear(dateText) {
    const m = String(dateText || '').match(/(\d{4})$/);
    return m ? Number(m[1]) : null;
  }

  function pickBestDateMatch(finding, matches) {
    const invoiceYear = getYear(finding.date);
    if (!invoiceYear) return matches[0];

    return [...matches].sort((a, b) => {
      const ay = getYear(a.installed) || 9999;
      const by = getYear(b.installed) || 9999;
      return Math.abs(ay - invoiceYear) - Math.abs(by - invoiceYear);
    })[0];
  }

  function today() {
    const d = new Date();
    return `${String(d.getMonth() + 1).padStart(2, '0')}/${String(d.getDate()).padStart(2, '0')}/${d.getFullYear()}`;
  }

  function waitUntil(fn, timeout = 5000, interval = 150) {
    return new Promise(resolve => {
      const start = Date.now();

      const timer = setInterval(() => {
        const result = fn();

        if (result) {
          clearInterval(timer);
          resolve(result);
        } else if (Date.now() - start > timeout) {
          clearInterval(timer);
          resolve(null);
        }
      }, interval);
    });
  }

