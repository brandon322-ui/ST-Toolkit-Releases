  function findActionControl(intent, root = document, controlSelector = 'button, [role="button"]', options = {}) {
    const selectors = ACTION_SELECTORS[intent] || [];
    const label = ACTION_LABELS[intent] || '';
    const isEligible = el =>
      el?.offsetParent !== null &&
      el.matches(controlSelector) &&
      (!options.enabledOnly || (!el.disabled && el.getAttribute('aria-disabled') !== 'true'));

    for (const selector of selectors) {
      const match = [...root.querySelectorAll(selector)].find(isEligible);
      if (match) return match;
    }

    const controls = [...root.querySelectorAll(controlSelector)].filter(isEligible);
    const accessibleMatch = controls.find(el =>
      label && normalizeSpaces(getAccessibleName(el)).toLowerCase() === label.toLowerCase()
    );
    if (accessibleMatch) return accessibleMatch;

    // Last-resort compatibility fallback for legacy markup with no semantic
    // attributes. Keeping this preserves current behavior during UI rollouts.
    return controls.find(el =>
      label && normalizeSpaces(el.innerText).toLowerCase() === label.toLowerCase()
    ) || null;
  }

  function getAccessibleName(el) {
    if (!el) return '';

    const direct = el.getAttribute('aria-label') || el.getAttribute('title');
    if (direct) return normalizeSpaces(direct);

    const labelledBy = (el.getAttribute('aria-labelledby') || '')
      .split(/\s+/)
      .filter(Boolean)
      .map(id => document.getElementById(id)?.innerText || '')
      .join(' ');
    if (labelledBy) return normalizeSpaces(labelledBy);

    if (el.id) {
      const label = [...document.querySelectorAll('label[for]')]
        .find(item => item.htmlFor === el.id);
      if (label) return normalizeSpaces(label.innerText);
    }

    return normalizeSpaces(el.innerText || el.value || '');
  }

