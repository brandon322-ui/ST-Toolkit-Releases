  function waitForStart() {
    clearMismatchedStateForPage();
    let tries = 0;

    const timer = setInterval(() => {
      tries++;

      const state = getState();
      const shouldShow =
        isInvoicePage() ||
        location.href.includes('/equipment/table') ||
        (
          state?.step === 'open-equipment-table' &&
          state?.locationId &&
          location.href.toLowerCase().includes(`/location/${state.locationId}/equipment/table`)
        );

      if (shouldShow || getPageType() === 'other') {
        clearInterval(timer);

        if (shouldShow || manualLauncherOpen) {
          buildPanel();
        } else {
          removePanel();
          createLauncher();
        }

        setTimeout(autoContinue, 750);
      }

      if (tries >= 40) clearInterval(timer);
    }, 500);
  }

  function handleRouteChange() {
    if (location.href === lastRouteKey) return;

    lastRouteKey = location.href;
    clearMismatchedStateForPage();

    const pageType = getPageType();
    if (pageType === 'invoice' || pageType === 'equipmentTable') {
      if (!document.getElementById(TOOL_ID)) buildPanel();
      else {
        updatePageActions();
        restorePanelState();
      }
      setTimeout(autoContinue, 250);
      return;
    }

    if (!manualLauncherOpen) {
      removePanel();
      createLauncher();
    } else {
      updatePageActions();
    }
  }

