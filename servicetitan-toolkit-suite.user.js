    function normalizeQueue(queue) {
        if (!Array.isArray(queue)) return [];

        const seen = new Set();
        return queue.flatMap(item => {
            if (!item || typeof item !== 'object') return [];

            const invoiceNumber = typeof item.invoiceNumber === 'string' ? item.invoiceNumber : '';
            if (!/^\d+$/.test(invoiceNumber) || seen.has(invoiceNumber)) return [];
            if (getInvoiceNumberFromUrl(item.url) !== invoiceNumber) return [];

            seen.add(invoiceNumber);
            return [{
                invoiceNumber,
                url: cleanInvoiceUrl(item.url),
                reviewedAt: typeof item.reviewedAt === 'string' ? item.reviewedAt : null
            }];
        });
    }

    function normalizeActiveBatch(batch) {
        if (!batch || typeof batch !== 'object' || Array.isArray(batch)) return null;
        if (typeof batch.batchName !== 'string' || !batch.batchName.trim()) return null;

        return {
            batchName: batch.batchName.trim(),
            setAt: typeof batch.setAt === 'string' ? batch.setAt : null
        };
    }

    function normalizeInvoiceNumbers(values) {
        if (!Array.isArray(values)) return [];
        return [...new Set(values.filter(value => typeof value === 'string' && /^\d+$/.test(value)))];
    }

    function normalizeRunner(runner) {
        if (!runner || typeof runner !== 'object' || Array.isArray(runner)) return null;

        const hasValidOwner = typeof runner.ownerTabId === 'string' && runner.ownerTabId.length > 0;
        const hasValidBatch = typeof runner.batchName === 'string' && runner.batchName.length > 0;
        const hasValidRemaining = Array.isArray(runner.remaining);
        const hasValidUrls = runner.urls && typeof runner.urls === 'object' && !Array.isArray(runner.urls);
        const remaining = normalizeInvoiceNumbers(runner.remaining);
        const urls = hasValidUrls
            ? Object.fromEntries(Object.entries(runner.urls).filter(
                ([invoiceNumber, url]) => /^\d+$/.test(invoiceNumber) && typeof url === 'string' && url.length > 0
            ))
            : {};
        const hasCompleteUrls = remaining.every(invoiceNumber => Boolean(urls[invoiceNumber]));

        return {
            ...runner,
            running: runner.running === true && hasValidOwner && hasValidBatch && hasValidRemaining && hasValidUrls && hasCompleteUrls,
            ownerTabId: hasValidOwner ? runner.ownerTabId : null,
            batchName: hasValidBatch ? runner.batchName : '',
            remaining,
            urls,
            successes: normalizeInvoiceNumbers(runner.successes),
            failures: Array.isArray(runner.failures)
                ? runner.failures
                    .filter(failure => failure && typeof failure.invoiceNumber === 'string' && /^\d+$/.test(failure.invoiceNumber))
                    .map(failure => ({
                        invoiceNumber: failure.invoiceNumber,
                        error: typeof failure.error === 'string' ? failure.error : 'Unknown error'
                    }))
                : [],
            currentInvoice: typeof runner.currentInvoice === 'string' ? runner.currentInvoice : null,
            heartbeatAt: typeof runner.heartbeatAt === 'string' ? runner.heartbeatAt : null
        };
    }

    function normalizeMaterialCleanup(cleanup) {
        if (!cleanup || typeof cleanup !== 'object' || Array.isArray(cleanup)) return null;

        return {
            running: cleanup.running === true,
            invoiceNumber: typeof cleanup.invoiceNumber === 'string' && /^\d+$/.test(cleanup.invoiceNumber)
                ? cleanup.invoiceNumber
                : null,
            total: Number.isFinite(Number(cleanup.total)) ? Number(cleanup.total) : null,
            deleted: Number.isFinite(Number(cleanup.deleted)) ? Number(cleanup.deleted) : 0,
            message: typeof cleanup.message === 'string' ? cleanup.message : null,
            at: typeof cleanup.at === 'string' ? cleanup.at : null
        };
    }

    function normalizeOperationalReadiness(readiness) {
        if (!readiness || typeof readiness !== 'object' || Array.isArray(readiness)) return null;

        const checks = Array.isArray(readiness.checks)
            ? readiness.checks
                .filter(check => check && typeof check === 'object')
                .map(check => ({
                    id: typeof check.id === 'string' ? check.id : '',
                    label: typeof check.label === 'string' ? check.label : '',
                    status: ['pass', 'fail', 'unknown'].includes(check.status) ? check.status : 'unknown',
                    displayText: typeof check.displayText === 'string' ? check.displayText : '',
                    isBlocker: check.isBlocker === true
                }))
                .filter(check => check.id && check.label && check.displayText)
            : [];

        return {
            invoiceNumber: typeof readiness.invoiceNumber === 'string' && /^\d+$/.test(readiness.invoiceNumber)
                ? readiness.invoiceNumber
                : null,
            checks,
            at: typeof readiness.at === 'string' ? readiness.at : null
        };
    }

