    async function cleanBadMaterials(silent = false, options = {}) {
        try {
            const activeCleanup = Store.getMaterialCleanup();
            if (activeCleanup?.running) {
                createBox();
                return {
                    inProgress: true,
                    startCount: activeCleanup.total || 0,
                    remaining: activeCleanup.total === null ? 0 : Math.max(activeCleanup.total - activeCleanup.deleted, 0),
                    deleted: activeCleanup.deleted
                };
            }

            const invoiceNumber = getInvoiceNumber();
            const reloadAfter = options.reloadAfter !== false;
            setMaterialCleanupProgress({ invoiceNumber });

            const startBadMaterials = await fetchBadMaterials(invoiceNumber);
            const startCount = startBadMaterials.length;
            setMaterialCleanupProgress({ invoiceNumber, deleted: 0, total: startCount });

            if (startCount === 0) {
                Store.clearMaterialCleanup();
                if (!silent) setToolkitMessage('No bad material quantity rows found.');
                createBox();
                return { startCount: 0, remaining: 0, deleted: 0 };
            }

            let deleted = 0;
            let attempts = 0;

            while (attempts < startCount + 10) {
                const badMaterials = await fetchBadMaterials(invoiceNumber);
                const nextMaterial = badMaterials[0];

                if (!nextMaterial) break;

                await deleteMaterialById(nextMaterial.Id);
                await waitForMaterialDeleted(nextMaterial.Id, invoiceNumber);
                deleted++;
                attempts++;
                setMaterialCleanupProgress({ invoiceNumber, deleted, total: startCount });
            }

            const remaining = (await fetchBadMaterials(invoiceNumber)).length;
            Store.clearMaterialCleanup();

            if (!silent) {
                setToolkitMessage(`Finished cleaning materials. Deleted ${deleted}. Remaining bad rows: ${remaining}.`);
            }

            createBox();
            if (reloadAfter && deleted > 0) refreshInvoiceAfterMaterialCleanup();
            return { startCount, remaining, deleted };
        } catch (err) {
            Store.clearMaterialCleanup();
            setToolkitMessage(err.message);
            createBox();
            throw err;
        }
    }

    async function cleanAndMarkReviewed() {
        try {
            const result = await cleanBadMaterials(true, { reloadAfter: false });
            if (result.inProgress) return;

            if (result.remaining > 0) {
                setToolkitMessage(`Materials cleaned partially. Deleted ${result.deleted}. ${result.remaining} bad rows still remain. Not marked reviewed.`);
                createBox();
                return;
            }

            if (!markReviewed(false)) return;
            setToolkitMessage(`Materials cleaned and invoice marked reviewed. Deleted ${result.deleted}. Remaining bad rows: ${result.remaining}.`);
            createBox();
            if (result.deleted > 0) refreshInvoiceAfterMaterialCleanup();
        } catch (err) {
            setToolkitMessage(`Clean + Review failed: ${err.message}`);
            createBox();
        }
    }

    function markReviewed(showMessage = true) {
        const invoiceNumber = getInvoiceNumber();

        if (!invoiceNumber) {
            setToolkitMessage('Could not find invoice number.');
            createBox();
            return false;
        }

        if (isCurrentlyBatchedInServiceTitan()) {
            setToolkitMessage(`Invoice ${invoiceNumber} is already batched in ServiceTitan.`);
            createBox();
            return false;
        }

        const operationalBlockers = OperationalReadiness.getBlockers(invoiceNumber);
        if (operationalBlockers.length) {
            setToolkitMessage(`Cannot mark reviewed: ${operationalBlockers.map(check => check.displayText).join('; ')}.`);
            createBox();
            return false;
        }

        const queue = Store.getQueue();

        if (!isInQueue(invoiceNumber)) {
            queue.push({
                invoiceNumber,
                url: cleanInvoiceUrl(window.location.href),
                reviewedAt: new Date().toISOString()
            });
            Store.saveQueue(queue);
        }

        markReviewedTab();

        if (showMessage) setToolkitMessage(`Invoice ${invoiceNumber} marked reviewed.`);
        createBox();
        return true;
    }

